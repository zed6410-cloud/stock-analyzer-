export function searchKrStocks(query) {
  const q = query.trim().toLowerCase();
  if (!q) return [];
  const seen = new Set();
  const results = [];
  for (const s of KR_STOCKS) {
    if (s.name.toLowerCase().includes(q) && !seen.has(s.symbol)) {
      seen.add(s.symbol);
      results.push(s);
    }
  }
  return results.slice(0, 15);
}
